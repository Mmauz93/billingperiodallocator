function DropdownMenu({
  onOpenChange,
  open: controlledOpen,
  defaultOpen,
  modal = false,
  ...props
}: React.ComponentProps<typeof DropdownMenuPrimitive.Root>) {
  const [isInternalOpen, setIsInternalOpen] = React.useState(
    defaultOpen ?? false,
  );

  // Determine the actual open state (controlled or internal)
  const isActuallyOpen =
    controlledOpen !== undefined ? controlledOpen : isInternalOpen;

  const handleOpenChange = React.useCallback(
    (openParam: boolean) => {
      if (controlledOpen === undefined) {
        // Only update internal state if not controlled
        setIsInternalOpen(openParam);
      }
      if (onOpenChange) {
        onOpenChange(openParam);
      }
    },
    [onOpenChange, controlledOpen],
  );

  return (
    <DropdownMenuPrimitive.Root
      data-slot="dropdown-menu"
      {...props}
      open={isActuallyOpen}
      onOpenChange={handleOpenChange}
      modal={modal}
    />
  );
}

